package org.samplr.server.model;

import static org.junit.Assert.assertEquals;
import static org.junit.Assert.assertNotNull;
import static org.junit.Assert.fail;

import org.junit.Before;
import org.junit.Test;

public class SampleSourceTypeTest {
  private SampleSourceType sampleSourceType;

  @Before
  public void setUp() {
    sampleSourceType = new SampleSourceType("Title", "title");
  }

  @Test
  public void testSampleSourceType() {
    assertNotNull(sampleSourceType);
  }

  @Test
  public void testSampleSourceType_Null_Title() {
    try {
      new SampleSourceType(null, "title");
      fail("Should have raised exception.");
    } catch (final NullPointerException e) {
    }
  }

  @Test
  public void testSampleSourceType_Null_NormalizedTitle() {
    try {
      new SampleSourceType("Title", null);
      fail("Should have raised exception.");
    } catch (final NullPointerException e) {
    }
  }

  @Test
  public void testSetTitle() {
    sampleSourceType.setTitle("foo");

    assertEquals("foo", sampleSourceType.getTitle());
  }

  @Test
  public void testSetTitle_Null() {
    try {
      sampleSourceType.setTitle(null);
      fail("Should have raised exception.");
    } catch (final NullPointerException e) {
    }
  }

  @Test
  public void testSetNormalizedTitle() {
    sampleSourceType.setNormalizedTitle("foo");

    assertEquals("foo", sampleSourceType.getNormalizedTitle());
  }

  @Test
  public void testSetNormalizedTitle_Null() {
    try {
      sampleSourceType.setNormalizedTitle(null);
      fail("Should have raised exception.");
    } catch (final NullPointerException e) {
    }
  }

  @Test
  public void testGetTitle() {
    assertEquals("Title", sampleSourceType.getTitle());
  }

  @Test
  public void testGetNormalizedTitle() {
    assertEquals("title", sampleSourceType.getNormalizedTitle());
  }

}
